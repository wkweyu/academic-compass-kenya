ADD COLUMN IF NOT EXISTS weekly_workload_limit INTEGER DEFAULT 28,
ADD COLUMN IF NOT EXISTS current_workload INTEGER DEFAULT 0;

-- Create teacher availability table
CREATE TABLE IF NOT EXISTS public.teacher_availability (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  teacher_id BIGINT NOT NULL REFERENCES public.teachers(id) ON DELETE CASCADE,
  day_of_week INTEGER NOT NULL CHECK (day_of_week BETWEEN 0 AND 6), -- 0=Sunday, 1=Monday, etc.
  start_time TIME NOT NULL DEFAULT '08:00',
  end_time TIME NOT NULL DEFAULT '17:00',
  is_available BOOLEAN DEFAULT true,
  reason TEXT,
  school_id BIGINT NOT NULL,
  created_at TIMESTAMPTZ DEFAULT now(),
  updated_at TIMESTAMPTZ DEFAULT now(),
  UNIQUE(teacher_id, day_of_week)
);

-- Create teacher specializations table (subjects they can teach)
CREATE TABLE IF NOT EXISTS public.teacher_specializations (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  teacher_id BIGINT NOT NULL REFERENCES public.teachers(id) ON DELETE CASCADE,
  subject_id BIGINT NOT NULL REFERENCES public.subjects(id) ON DELETE CASCADE,
  proficiency_level VARCHAR(20) DEFAULT 'Intermediate' CHECK (proficiency_level IN ('Beginner', 'Intermediate', 'Advanced', 'Expert')),
  is_primary BOOLEAN DEFAULT false,
  years_experience INTEGER DEFAULT 0,
  school_id BIGINT NOT NULL,
  created_at TIMESTAMPTZ DEFAULT now(),
  UNIQUE(teacher_id, subject_id)
);

-- Create teacher workload tracking table
CREATE TABLE IF NOT EXISTS public.teacher_workload (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  teacher_id BIGINT NOT NULL REFERENCES public.teachers(id) ON DELETE CASCADE,
  class_id BIGINT NOT NULL REFERENCES public.classes(id) ON DELETE CASCADE,
  stream_id BIGINT REFERENCES public.streams(id) ON DELETE SET NULL,
  subject_id BIGINT NOT NULL REFERENCES public.subjects(id) ON DELETE CASCADE,
  lessons_per_week INTEGER NOT NULL DEFAULT 1,
  is_class_teacher BOOLEAN DEFAULT false,
  academic_year INTEGER NOT NULL DEFAULT EXTRACT(YEAR FROM CURRENT_DATE),
  term INTEGER NOT NULL DEFAULT 1,
  school_id BIGINT NOT NULL,
  created_at TIMESTAMPTZ DEFAULT now(),
  updated_at TIMESTAMPTZ DEFAULT now(),
  UNIQUE(teacher_id, class_id, stream_id, subject_id, academic_year, term)
);

