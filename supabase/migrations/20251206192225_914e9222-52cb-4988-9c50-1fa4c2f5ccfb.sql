-- Function to calculate teacher workload
CREATE OR REPLACE FUNCTION public.calculate_teacher_workload(p_teacher_id BIGINT)
RETURNS INTEGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = 'public'
AS $$
DECLARE
  total_lessons INTEGER;
BEGIN
  SELECT COALESCE(SUM(lessons_per_week), 0) INTO total_lessons
  FROM public.teacher_workload
  WHERE teacher_id = p_teacher_id
    AND academic_year = EXTRACT(YEAR FROM CURRENT_DATE);
  
  -- Update the teacher's current workload
  UPDATE public.teachers SET current_workload = total_lessons WHERE id = p_teacher_id;
  
  RETURN total_lessons;
END;
$$;

-- Function to check if teacher can teach a subject
CREATE OR REPLACE FUNCTION public.can_teacher_teach_subject(p_teacher_id BIGINT, p_subject_id BIGINT)
RETURNS BOOLEAN
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = 'public'
AS $$
BEGIN
  RETURN EXISTS (
    SELECT 1 FROM public.teacher_specializations
    WHERE teacher_id = p_teacher_id AND subject_id = p_subject_id
  );
END;
$$;

-- Trigger to update workload when assignments change
CREATE OR REPLACE FUNCTION public.update_teacher_workload_trigger()
RETURNS TRIGGER
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = 'public'
AS $$
BEGIN
  IF TG_OP = 'INSERT' OR TG_OP = 'UPDATE' THEN
    PERFORM calculate_teacher_workload(NEW.teacher_id);
    RETURN NEW;
  ELSIF TG_OP = 'DELETE' THEN
    PERFORM calculate_teacher_workload(OLD.teacher_id);
    RETURN OLD;
  END IF;
END;
$$;

DROP TRIGGER IF EXISTS trigger_update_teacher_workload ON public.teacher_workload;
CREATE TRIGGER trigger_update_teacher_workload
  AFTER INSERT OR UPDATE OR DELETE ON public.teacher_workload
  FOR EACH ROW
  EXECUTE FUNCTION update_teacher_workload_trigger();

